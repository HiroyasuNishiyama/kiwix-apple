import SwiftUI

struct GridSection<Content: View>: View {
    let title: String
    let content: Content
    
    init(title: String, @ViewBuilder content: () -> Content) {
        self.title = title
        self.content = content()
    }
    
    var body: some View {
        Section {
            content
        } header: {
            Text(title).font(.title3).fontWeight(.semibold)
        }
    }
}


